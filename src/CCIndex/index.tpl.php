<h2>Installation</h2>
<p>Download the latest build at <a href='https://github.com/MickeMakaron/Pretto/tree/v0.15'>github</a> and extract the contents to your site's root.</p>
<p>Allow writing to the data folder at "site/data" (777) and the database file .ht.sqlite (666). If you want to use the standard grid theme, you must also allow writing to "themes/grid" (777).</p>
<p>For some web servers you must rewrite base URL by adding a RewriteBase in .htaccess.</p>
<p>Lastly, go to your site through your browser and press the installation link. That's it!</p>